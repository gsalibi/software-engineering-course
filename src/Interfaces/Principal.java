    private void criarTarefaJButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_criarTarefaJButtonMouseClicked
        if (criadorTarefaJPanel.isEnabled() == false) {
            // Desliga tarefas
            // Liga o painel
            criadorTarefaJPanel.setEnabled(true);
            criadorTarefaJPanel.setVisible(true);

            // Inicializa os campos da nova tarefa
            nomeTarefaJTextField.setText("Nome da tarefa");
            Calendar hoje = Calendar.getInstance();
            Integer dia = hoje.get(Calendar.DAY_OF_MONTH);
            Integer mes = hoje.get(Calendar.MONTH);
            Integer ano = hoje.get(Calendar.YEAR);
            diaJTextField.setText((++dia).toString());
            mesJTextField.setText((++mes).toString());
            anoJTextField.setText(ano.toString());
            progressoTarefaJTextField.setText("0");
            descricaoJTextArea.setText("DescriÃ§Ã£o da tarefa aqui");
            atribuidos = new ArrayList<>();

            responsaveisListModel.clear();
            membrosListModel.clear();
            for (int i = 0; i < sistema.getUsuarios().size(); i++) {
                membrosListModel.addElement(sistema.getUsuarios().get(i).getNome());
            }
            responsaveisJList.setModel(responsaveisListModel);
            membrosJList.setModel(membrosListModel);

        } else {
            // Liga tarefas
            // Desliga o Painel
            criadorTarefaJPanel.setEnabled(false);
            criadorTarefaJPanel.setVisible(false);
        }
    }//GEN-LAST:event_criarTarefaJButtonMouseClicked

