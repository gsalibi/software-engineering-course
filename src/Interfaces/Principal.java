    private void ligarBarras(boolean ligar) {
        if (ligar) {
            for (BarraTarefa bt : barras) {
                bt.setVisible(false);
                bt.setEnabled(false);

                posicaoProximaBarra.x = posicaoProximaBarraInicial.x;
                posicaoProximaBarra.y = posicaoProximaBarraInicial.y;
            }
            barras.clear();

            for (Tarefa t : user.getTarefas()) {
                BarraTarefa bt = new BarraTarefa(t, posicaoProximaBarra, false);
                barras.add(bt);
                posicaoProximaBarra.y += 80;
                progressoTarefasJPanel.add(bt);
                bt.setEnabled(true);
                bt.setVisible(true);
            }
        }
    }

