    private void adicionarJButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_adicionarJButtonMouseClicked
        if (!membrosJList.isSelectionEmpty()) {
            // Adicionar na JList dos responsÃ¡veis
            int index = membrosJList.getSelectedIndex();
            String nome = membrosJList.getSelectedValue();
            responsaveisListModel.addElement(membrosJList.getSelectedValue());
            responsaveisJList.setModel(responsaveisListModel);

            // Remove da JList dos membros
            membrosListModel.remove(index);
            membrosJList.setModel(membrosListModel);
            membrosJList.clearSelection();

            for (Usuario usr : sistema.getUsuarios()) {
                if (usr.getNome().equals(nome)) {
                    atribuidos.add(usr);
                    break;
                }
            }

            //System.out.println(atribuidos);
        }
    }//GEN-LAST:event_adicionarJButtonMouseClicked

