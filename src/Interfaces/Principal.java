    private void concluirJButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_concluirJButtonMouseClicked
        String nomeTarefa = nomeTarefaJTextField.getText();
        String descricaoTarefa = descricaoJTextArea.getText();
        int progressoTarefa = Integer.valueOf(progressoTarefaJTextField.getText());

        String diaPrazo = diaJTextField.getText();
        String mesPrazo = mesJTextField.getText();
        String anoPrazo = anoJTextField.getText();

        SimpleDateFormat formatoData = new SimpleDateFormat("dd/MM/yyyy");

        String s = diaPrazo;
        s += "/";
        s += mesPrazo;
        s += "/";
        s += anoPrazo;

        Date dataHoje = new Date();
        Date dataPrazo = null;

        boolean podeGravarTarefa = true;
        
        

        try {
            dataPrazo = formatoData.parse(s);
            
            if (dataPrazo.before(dataHoje)) {
                // Mensagem de alerta
                JOptionPane.showMessageDialog(new Frame(), "Data de prazo invÃ¡lida!");
                podeGravarTarefa = false;
            }
        } catch (ParseException e) {
            // Mensagem de alerta
            podeGravarTarefa = false;
        }
        
        if (atribuidos.isEmpty()) {
            JOptionPane.showMessageDialog(new Frame(), "VocÃª precisa atribuir ao menos 1 pessoa Ã  tarefa.");
            podeGravarTarefa = false;
        }

        if (podeGravarTarefa) {
            tarefa = new Tarefa("Nova Tarefa", "Insira descricao da tarefa aqui", new Date());
            for (Usuario u : atribuidos) {
                tarefa.addUsuarioAtribuido(u);
                u.adicionaNovaTarefa(tarefa);
                //System.out.println("Tarefa atribuÃ­da ao usuÃ¡rio " + u.getNome());
            }

            if (dataPrazo != null) {
                tarefa.setPrazo(dataPrazo);
            }

            tarefa.setDescricao(descricaoTarefa);
            tarefa.setNome(nomeTarefa);
            tarefa.setProgresso(progressoTarefa);
            
            // Desliga o painel criador de nova tarefa
            criadorTarefaJPanel.setEnabled(false);
            criadorTarefaJPanel.setVisible(false);

            // Liga todas as barras de tarefa
            ligarBarras(true);
        }

    }//GEN-LAST:event_concluirJButtonMouseClicked

